void drawPixel(int x, int y, uint16_t color) {
    if (x >= 0 && x < 240 && y >= 0 && y < 160) {
        MODE3_VIDEO[y * 240 + x] = color;
    }
}

void drawText(char* text, int x, int y, uint16_t color) {
    while (*text) {
        unsigned char c = (unsigned char)*text;
        for (int i = 0; i < 8; i++) {
            for (int j = 0; j < 8; j++) {
                if (simpleFont[c][i] & (1 << (7 - j))) {
                    drawPixel(x + j, y + i, color);
                }
            }
        }
        x += 8;
        text++;
    }
}

void drawNumber(int num, int x, int y, uint16_t color) {
    char buffer[12];
    int i = 0;
    if (num == 0) {
        buffer[i++] = '0';
    } else {
        int temp = num;
        while (temp > 0) {
            buffer[i++] = (temp % 10) + '0';
            temp /= 10;
        }
        for(int j = 0; j < i / 2; j++) {
            char t = buffer[j];
            buffer[j] = buffer[i - 1 - j];
            buffer[i - 1 - j] = t;
        }
    }
    buffer[i] = '\0';
    drawText(buffer, x, y, color);
}

void drawTextBox(char* line1, char* line2) {
    for(int r = 112; r < 160; r++) {
        for(int c = 0; c < 240; c++) {
            uint16_t color = 0x4A69; 
            if (r < 114 || r > 157 || c < 2 || c > 237) color = 0x7FFF; 
            else if (r == 114 || r == 157 || c == 2 || c == 237) color = 0x0000; 
            drawPixel(c, r, color);
        }
    }
    if (line1 != NULL) drawText(line1, 10, 122, 0x7FFF);
    if (line2 != NULL) drawText(line2, 10, 137, 0x7FFF);
}

void fillScreen(uint16_t color) {
    for (int i = 0; i < 240 * 160; i++) MODE3_VIDEO[i] = color;
}

void initOAM() {
    uint32_t* oam = (uint32_t*)0x07000000;
    for (int i = 0; i < 128 * 2; i++) oam[i] = 0x0200; 
}

void setSpriteAttribute(int id, int x, int y, int pal, int tile, int priority, int size) {
    volatile uint16_t* oam = (volatile uint16_t*)0x07000000;
    oam[id * 4 + 0] = (y & 0xFF); 
    oam[id * 4 + 1] = (x & 0x1FF) | (size << 14);
    oam[id * 4 + 2] = (tile & 0x3FF) | (priority << 10) | (pal << 12);
}

